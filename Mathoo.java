
package com.mycompany.mathoo;

public class Mathoo {

    public static void main(String[] args) {
        int x = 200;
        int y = -200;
        int z = 25;
        
        System.out.println(Math.PI);
        System.out.println("the squar root of the x: " + Math.sqrt(x));
        
        System.out.println("the minimum value of the x and y is: " + Math.min(x, y));
        
        System.out.println("the absolute value of y is: " + Math.abs(y));
        
        System.out.println("the result of z power of 2 is: " + Math.pow(z, 2));
        
        System.out.println("the maximum number of the two number: " + Math.max(y, z));

    }
}
